# method for preventing the overwite of existing output files
def duplicate_files(path):
    filename, extension = os.path.splitext(path)
    cnt = 1

    while os.path.exists(path):
        path = filename + " (" + str(cnt) + ")" +extension
        cnt += 1
    return path

